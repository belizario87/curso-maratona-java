package br.com.cursojava.introducao.arrays;

public class Arrays1 {
    public static void main(String[] args) {
        int[] idade = new int[3];
        idade[0] = 19;
        idade[1] = 21;
        idade[2] = 35;

        System.out.println("A idade 1 eh: " + idade[0]);

        System.out.println("A idade 2 eh: " + idade[1]);

        System.out.println("A idade 3 eh: " + idade[2]);
    }
}
