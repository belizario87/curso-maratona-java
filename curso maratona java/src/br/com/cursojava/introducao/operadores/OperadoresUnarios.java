package br.com.cursojava.introducao.operadores;

public class OperadoresUnarios {
    public static void main(String[] args) {
        float numero1 = 10;
        float numero2 = 20;
        float divisao = numero1 / numero2;
        System.out.println("O resultado da divisao e: " + divisao);
    }
}
